  constructor(iv: Uint8Array, mapleVersion: number) {
    this.mapleVersion = (mapleVersion >> 8) | ((mapleVersion << 8) & 0xff00);
    this.iv = iv;
  }

  public encrypt(packet: Buffer) {
    let buff: Buffer = Buffer.from(packet);
    let iv = Buffer.from(this.iv);

    for (let i = 0; i < buff.length; i++) {
      let byte = buff[i];
      byte = ((byte << 4) & 0xf0) | ((byte >> 4) & 0x0f);
      byte =
        ((byte << 1) & 0xfe) ^
        ((((byte << 1) & 0xfe) ^ ((byte >> 1) & 0x7f)) & 0x55);
      byte ^= PacketCrypto.funnyBytes[iv[0]];
      iv = PacketCrypto.generateIv(buff[i], iv);
      buff[i] = byte;
    }
    this.iv = PacketCrypto.getNewIv(this.iv);
    return buff;
  }

  public decrypt(packet: Buffer) {
    let buff: Buffer = Buffer.from(this.iv);

    for (let x = 0; x < packet.length; x++) {
      let byte = packet[x];
      byte ^= PacketCrypto.funnyBytes[buff[0]];
      byte =
        ((byte << 1) & 0xfe) ^
        ((((byte << 1) & 0xfe) ^ ((byte >> 1) & 0x7f)) & 0x55);
      byte = ((byte << 4) & 0xf0) | ((byte >> 4) & 0x0f);
      buff = PacketCrypto.generateIv(byte, buff);
      packet[x] = byte;
    }
    this.iv = PacketCrypto.getNewIv(this.iv);
    return packet;
  }

  public static getNewIv(oldIv: Uint8Array) {
    let magic = Buffer.from([0xf2, 0x53, 0x50, 0xc6]);
    for (let x = 0; x < 4; x++) {
      magic = PacketCrypto.generateIv(oldIv[x], magic);
    }
    return magic;
  }

  public static generateIv(inputByte: number, buff: Buffer): Buffer {
    buff[0] += PacketCrypto.funnyBytes[buff[1]] - inputByte;
    buff[1] -= buff[2] ^ PacketCrypto.funnyBytes[inputByte];
    buff[2] ^= inputByte + PacketCrypto.funnyBytes[buff[3]];
    buff[3] = buff[3] - buff[0] + PacketCrypto.funnyBytes[inputByte];

    let ret_value = buff[0];
    ret_value |= (buff[1] << 8) & 0xff00;
    ret_value |= (buff[2] << 16) & 0xff0000;
    ret_value |= (buff[3] << 24) & 0xff000000;
    ret_value = ((ret_value << 3) & 0xfffffff8) | ((ret_value >> 29) & 7);

    buff[0] = ret_value;
    buff[1] = ret_value >> 8;
    buff[2] = ret_value >> 16;
    buff[3] = ret_value >> 24;

    return buff;
  }

  public static getPacketLength(packetHeader: number) {
    let packetLength = (packetHeader >>> 16) ^ (packetHeader & 0xffff);
    packetLength =
      ((packetLength >> 8) & 0xff00) | ((packetLength >>> 8) & 0xff);
    return packetLength;
  }

